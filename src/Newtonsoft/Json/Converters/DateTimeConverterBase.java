public abstract class DateTimeConverterBase extends JsonConverter {
  protected NObject javonetHandle;

  public DateTimeConverterBase(NObject handle) {
    super(handle);
    this.javonetHandle = handle;
  }

  public void setJavonetHandle(NObject handle) {
    this.javonetHandle = handle;
  }
  /** Method */

  @MethodTypeAnnotation(type = "Method")
  public java.lang.Boolean CanConvert(jio.System.Type objectType) {
    try {
      java.lang.Boolean res = javonetHandle.invoke("CanConvert", objectType);
      if (res == null) return false;
      return res;
    } catch (JavonetException _javonetException) {
      _javonetException.printStackTrace();
      return false;
    }
  }

  static {
    try {
      Activation.initializeJavonet();
    } catch (java.lang.Exception e) {
      e.printStackTrace();
    }
  }
}
